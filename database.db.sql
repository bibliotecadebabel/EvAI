	"id"	INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	"id_test"	INTEGER NOT NULL,
	"iteration"	INTEGER NOT NULL,
	"dna"	TEXT NOT NULL,
	"tangentPlane"	TEXT NOT NULL,
	"center"	INTEGER NOT NULL
